package kotlinforookies.essentials

//To print to the console:
println("What you want to print")

//A variable is a storage box for information
//To create a variable you don't want to change later (immutable variable):
val myName = "Asaiah"
println(myName) //See the variable's value

//To create a variable you want to change later (mutable variable):
var myAge = 17
println("My age: $myAge") //See the variable's value

//To change the value of a variable
myAge = 25
println("My new age: $myAge") //See the variable's new value